#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*temp;
	size_t			i;

	temp = b;
	i = 0;
	while (i < len)
	{
		*temp++ = c;
		++i;
	}
	return (b);
}
